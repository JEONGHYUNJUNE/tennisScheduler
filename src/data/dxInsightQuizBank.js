export function getChapterMeta(chapterId) {
  return dxInsightParts.flatMap((part) => part.chapters.map((chapter) => ({ ...chapter, partId: part.id, partTitle: part.title }))).find((chapter) => chapter.id === chapterId)
}

export function buildTermQuestions() {
  const definitions = dxInsightTerms.map((item) => item.definition)
  return dxInsightTerms.map((item, index) => {
    const wrong = definitions.filter((definition) => definition !== item.definition).sort(() => Math.random() - 0.5).slice(0, 3)
    const options = [item.definition, ...wrong].sort(() => Math.random() - 0.5)
    return {
      id: `${item.id}-quiz`,
      chapterId: item.chapterId,
      type: 'term',
      prompt: `"${item.term}"의 정의로 가장 적절한 것은?`,
      options,
      answerIndex: options.indexOf(item.definition),
      explanation: `${item.term}: ${item.definition}`,
      term: item.term,
      seedIndex: index,
    }
  })
}
